SELECT *
	FROM DEPT d JOIN DEPT_TEMP dt  -- 이너조인은 중복결과만
	ON d.DNAME = dt.DNAME ;

--LEFT OUTER JOIN은 왼쪽이 다 나옴
SELECT *
	FROM DEPT d LEFT JOIN DEPT_TEMP dt 
	ON d.DNAME = dt.DNAME ;

--RIGHT OUTER JOIN은 오른쪽이 다 나옴
SELECT *
	FROM DEPT d RIGHT JOIN DEPT_TEMP dt 
	ON d.DNAME = dt.DNAME ;

-- FULL OUTER JOIN은 ORACLE LEFT와 RIGHT OUTER JOIN을 동신에 보여줌
SELECT *
	FROM DEPT d FULL JOIN DEPT_TEMP dt 
	ON d.DNAME = dt.DNAME ;















